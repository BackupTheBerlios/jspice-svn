package org.openspice.jspice.arithmetic;

public final class Add extends BinaryArithmeticOperator {

	public Num applyNum( final Num x, final Num y ) {
		return x.add( y );
	}

	public static final Add ADD = new Add();

	public static final Num doAdd( final Number x, final Number y ) {
		return (Num)ADD.apply( x, y );
	}

}
